package com.example.a12sha.justanother;

public class staypoint {
    private double lat;
    private double lon;
    private String dat;
    private String tim;
    private int stayptId;
    private double staylatmean;
    private double staylonmean;
    private int stateId;
    private double statelatmean;
    private double statelonmean;

    public double getLat() {
        return lat;
    }

    public void setLat(double lat) {
        this.lat = lat;
    }

    public double getLon() {
        return lon;
    }

    public void setLon(double lon) {
        this.lon = lon;
    }

    public String getDat() {
        return dat;
    }

    public void setDat(String dat) {
        this.dat = dat;
    }

    public String getTim() {
        return tim;
    }

    public void setTim(String tim) {
        this.tim = tim;
    }

    public int getStayptId() {
        return stayptId;
    }

    public void setStayptId(int stayptId) {
        this.stayptId = stayptId;
    }

    public double getStaylatmean() {
        return staylatmean;
    }

    public void setStaylatmean(double staylatmean) {
        this.staylatmean = staylatmean;
    }

    public double getStaylonmean() {
        return staylonmean;
    }

    public void setStaylonmean(double staylonmean) {
        this.staylonmean = staylonmean;
    }

    public int getStateId() {
        return stateId;
    }

    public void setStateId(int stateId) {
        this.stateId = stateId;
    }

    public double getStatelatmean() {
        return statelatmean;
    }

    public void setStatelatmean(double statelatmean) {
        this.statelatmean = statelatmean;
    }

    public double getStatelonmean() {
        return statelonmean;
    }

    public void setStatelonmean(double statelonmean) {
        this.statelonmean = statelonmean;
    }

}
